pub fn add(first: f64, second: f64) -> f64 {
    return first + second;
}

pub fn subtract(first: f64, second: f64) -> f64 {
    return first - second;
}

pub fn divide(first: f64, second: f64) -> f64 {
    return first / second;
}

pub fn multiply(first: f64, second: f64) -> f64 {
    return first * second;
}